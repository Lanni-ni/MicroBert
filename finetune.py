def group_sentences(df):
    sentences = []
    current_tokens = []
    current_labels = []

    for _, row in df.iterrows():
        token = row["Token"]
        label = row["Label"]
        if row["is_sep"]:
            if current_tokens:
                sentences.append((current_tokens, current_labels))
                current_tokens = []
                current_labels = []
        else:
            current_tokens.append(token)
            current_labels.append(label)

    if current_tokens:
        sentences.append((current_tokens, current_labels))

    return sentences

